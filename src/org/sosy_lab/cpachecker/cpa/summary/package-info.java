/**
 * Framework for summary generation for functions.
 *
 * <p>Usage instructions:
 * <ul>
 *   <li>
 *    A top-level CPA is {@link org.sosy_lab.cpachecker.cpa.summary.summaryGeneration.GenSummaryCPA}.
 *    It operates over {@link org.sosy_lab.cpachecker.cpa.summary.summaryGeneration.SummaryState},
 *    which corresponds to a single summary,
 *    and is derived from the {@link org.sosy_lab.cpachecker.core.reachedset.ReachedSet}
 *    generated by the intraprocedural analysis.
 *   </li>
 *
 *   <li>
 *    {@link org.sosy_lab.cpachecker.cpa.summary.summaryGeneration.GenSummaryCPA}
 *    wraps {@link org.sosy_lab.cpachecker.cpa.summary.summaryUsage.TopLevelSummaryCPA},
 *    which is a container for the usual abstract state (normally,
 *    {@link org.sosy_lab.cpachecker.cpa.composite.CompositeState}
 *    or {@link org.sosy_lab.cpachecker.cpa.arg.ARGState}).
 *
 *    <p>This CPA guides the child analyses to use and generate summaries.
 *   </li>
 * </ul>
 */
package org.sosy_lab.cpachecker.cpa.summary;

